# lower checking
lowerString = "dicoding"
print(lowerString.islower())

# upper checking
upperString = "DICODING"
print(upperString.isupper())

# alphabet checking
alphabetString = "dicoding"
print(alphabetString.isalpha())

# alfanumerik checking
alnumString = "dicoding123"
print(alnumString.isalnum())

# decimal checking
numberString = "123"
print(numberString.isdecimal())

# space checking -> tab, whitespace, space, newline, dll
scapeString = "      \t\n"
print(scapeString.isspace())

# kapital
stringKapital = "Ilham Yudiatmoko"
print(stringKapital.istitle())