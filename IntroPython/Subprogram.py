# Subprogram is designed to perform frequently used operations in a program

# Function
def rectangularAreaFormula(length, width):
    rectangularArea = length * width
    return rectangularArea


firstRectangular = rectangularAreaFormula(2, 4)
print(firstRectangular)

secondRectangular = rectangularAreaFormula(4, 4)
print(secondRectangular)
